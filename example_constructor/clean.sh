#!/bin/bash

# Скрипт очистки демонстрационных данных SMPC
# Удаляет все сгенерированные файлы, кроме скриптов

echo "Очистка демонстрационных данных..."

# Удаление директорий с ключами и значениями
rm -rf secrets keys values

# Удаление конфигурационных файлов и результатов
rm -f smpc.conf demo-info.txt certificate.txt result-*.txt

# Удаление временных файлов (если есть)
rm -f .*.txt *.txt.aggc

echo "Очистка завершена. Остались только скрипты."
